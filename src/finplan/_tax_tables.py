"""Embedded federal tax tables for the finplan engine.

All dollar amounts are nominal (not inflation-adjusted) for the stated tax year.
The engine inflates 2025 brackets forward using simulated CPI each year (Option A).

Sources
-------
ORDINARY_BRACKETS   Tax Foundation historical income tax CSV (1862-2025).
                    URL: https://taxfoundation.org/data/all/federal/
                         historical-income-tax-rates-brackets/
                    Data file: data/Historical Income Tax Rates and Brackets,
                    1862-2025.csv  (fetched 2026-05-23)

STANDARD_DEDUCTION  Tax Policy Institute (1970-2024) and IRS (2025-2026).
                    Data file: data/standard deduction table.csv

LTCG_BRACKETS       2003-2017: thresholds derived from ordinary bracket
                    boundaries (LTCG rates were tied to ordinary bracket
                    membership; see data/cap_gains_1988-2013.pdf and
                    data/capital_gains_tax.xlsx).
                    2018-2025: explicit IRS thresholds from Rev. Proc.
                    2017-58 through 2024-61 (TCJA decoupled LTCG thresholds
                    from ordinary brackets starting 2018).
                    Rates: 0% / 15% / 20%.  The 3.8% NIIT (IRC §1411) is
                    NOT included here; it is applied separately in taxes.py.

UNIFORM_LIFETIME_TABLE  IRS Publication 590-B (2025), Appendix B, Table III.
                    Effective for distribution calendar years on or after
                    Jan 1, 2022 per Treas. Reg. §1.401(a)(9)-9 (TD 9930).
                    Used by IRA *owners* to calculate RMDs; NOT the Single
                    Life Expectancy table (Table I, for beneficiaries).

Format
------
ORDINARY_BRACKETS and LTCG_BRACKETS
    {year: {"single": [(rate, min_income), ...], "mfj": [...]}}
    Brackets are ordered lowest-to-highest by min_income.
    rate is a decimal (0.10, not 10.0).

STANDARD_DEDUCTION
    {year: {"single": amount_int, "mfj": amount_int}}

UNIFORM_LIFETIME_TABLE
    {age: distribution_period_float}
    Ages 72-120.  Use 120 for any age >= 120.
    RMD = prior_year_end_balance / distribution_period

Lookup helpers are at the bottom of this module.
"""

