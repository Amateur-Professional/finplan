FilingStatusKey = str  # "single" or "mfj"
BracketList = list[tuple[float, int]]  # [(rate, min_income), ...]


def get_ordinary_brackets(year: int, filing_status: FilingStatusKey) -> BracketList:
    """Return ordinary income brackets for *year* and *filing_status*.

    Falls back to the nearest earlier year if *year* is not in the table.
    Raises ``ValueError`` if no data is available at or before *year*.
    """
    for y in range(year, year - 200, -1):
        entry = ORDINARY_BRACKETS.get(y)
        if entry:
            return entry[filing_status]
    raise ValueError(f"No ordinary bracket data found for year {year}")


def get_standard_deduction(year: int, filing_status: FilingStatusKey) -> int:
    """Return the standard deduction for *year* and *filing_status*.

    Falls back to the nearest earlier year if *year* is not in the table.
    """
    for y in range(year, year - 60, -1):
        entry = STANDARD_DEDUCTION.get(y)
        if entry:
            return entry[filing_status]
    raise ValueError(f"No standard deduction data found for year {year}")


def get_ltcg_brackets(year: int, filing_status: FilingStatusKey) -> BracketList:
    """Return LTCG brackets for *year* and *filing_status*.

    Falls back to the nearest earlier year available (floor: 2003).
    """
    for y in range(year, 2002, -1):
        entry = LTCG_BRACKETS.get(y)
        if entry:
            return entry[filing_status]
    raise ValueError(f"No LTCG bracket data found for year {year} (floor: 2003)")


def get_rmd_distribution_period(age: int) -> float:
    """Return the Uniform Lifetime Table distribution period for *age*.

    Ages below 72 return ``float("inf")`` (no RMD required).
    Ages above 120 use 2.0 per the table.
    """
    if age < 72:
        return float("inf")
    return UNIFORM_LIFETIME_TABLE.get(min(age, 120), 2.0)
